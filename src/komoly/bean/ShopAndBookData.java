package komoly.bean;

public class ShopAndBookData {

	private String boltName;
	private int boltId;
	private int telNum;

	private int bookId;
	private int count;

	public String getBoltName() {
		return boltName;
	}

	public void setBoltName(String boltName) {
		this.boltName = boltName;
	}

	public int getBoltId() {
		return boltId;
	}

	public void setBoltId(int boltId) {
		this.boltId = boltId;
	}

	public int getTelNum() {
		return telNum;
	}

	public void setTelNum(int telNum) {
		this.telNum = telNum;
	}

	public int getBookId() {
		return bookId;
	}

	public void setBookId(int bookId) {
		this.bookId = bookId;
	}

	public int getCount() {
		return count;
	}

	public void setCount(int count) {
		this.count = count;
	}
}
